#define ROWS 32
#define COLS 64

void calculate_weight_distribution(uint64_t H[][COLS], int n, int k, uint64_t *distribution) {
    uint64_t rows = n - k;
    uint64_t num_subsets = 1 << rows;
    //int i, j, subset, weight;
    uint64_t weight;

    clock_t t0 = clock();

    // Iterate over all possible subsets
    for (uint64_t subset = 1; subset < num_subsets; subset++) {
        uint64_t combination_vector[COLS] = {0};

        // Check each row if it should be included in the current subset
        for (uint64_t i = 0; i < rows; i++) {
            if (subset & (1 << i)) {  // Check if the ith bit is set in the subset
                // XOR the current combination vector with the ith row of H
                for (uint64_t j = 0; j < COLS; j++) {
                    combination_vector[j] ^= H[i][j];
                }
            }
        }

        // Calculate the weight of the combination vector (count of 1's)
        weight = 0;
        for (uint64_t j = 0; j < COLS; j++) {
            weight += combination_vector[j];
        }

        distribution[weight]++;  // Update the weight distribution

        if (subset % 1000000 == 1 && subset > 1000) {
            clock_t t1 = clock();
            double time_taken = ((double)(t1 - t0)) / CLOCKS_PER_SEC;
            printf("Time taken: %f\n", time_taken);
            printf("%" PRIu64 "\t/\t%" PRIu64 "\n", subset, num_subsets);
            // printf("Time remaining: %f\n", time_taken * (double)((max_combinations - i) / i));
        }
    }

    // Print the weight distribution
    printf("Weight Distribution:\n");
    for (i = 0; i <= COLS; i++) {
        printf("%d ", distribution[i]);
    }
    printf("\n");
}

